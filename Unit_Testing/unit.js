export default function greet(name) {
  let result;
    switch (name) {
      case 'Elizabeth':
        result = `Hello,${name}`;
        break;
      case name.isUpperCase:
        result = `Hello, ${name}`;
        break;
      case name = []:
        result = `Hello, ${[...name]}`;
        break;
      default:
        result = "Hello there!";
    }
  return result;
}
 